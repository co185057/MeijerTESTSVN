namespace SSCOUIViews.Controls.Helpers
{
    /// <summary>
    /// This is the base class used for the loader status table in Retrofit
    /// </summary>
    public class LoaderStatusItem
    {
        /// <summary>
        /// Gets or sets the denomination of the loader
        /// </summary>
        public string ItemDenomination { get; set; }

        /// <summary>
        /// Gets or sets the count of notes in the loader
        /// </summary>
        public string ItemCount { get; set; }

        /// <summary>
        /// Gets or sets the total amount in the loader
        /// </summary>
        public string ItemAmount { get; set; }
    }
}
